#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*first;
	void	*buff;

	first = NULL;
	while (lst)
	{
		buff = (*f)(lst->content);
		new = ft_lstnew(buff);
		if (!new)
		{
			(*del)(buff);
			ft_lstclear(&first, del);
			return (NULL);
		}
		ft_lstadd_back(&first, new);
		lst = lst->next;
	}
	return (first);
}
